    private interface ProfileQuery {
        String[] PROJECTION = {
                ContactsContract.CommonDataKinds.Email.ADDRESS,
                ContactsContract.CommonDataKinds.Email.IS_PRIMARY,
        };

        int ADDRESS = 0;
        int IS_PRIMARY = 1;
    }

    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;
        private final Boolean mNewsletterChecked;

        UserLoginTask(String email, String password, Boolean newsletterChecked) {
            mEmail = email;
            mPassword = password;
            mNewsletterChecked = newsletterChecked;
        }

        @Override
        protected Boolean doInBackground(Void... params) {

            mAuth.createUserWithEmailAndPassword(mEmail, mPassword)
                    .addOnCompleteListener(RegisterActivity.this, new OnCompleteListener<AuthResult>() {
                        @Override
                        public void onComplete(@NonNull Task<AuthResult> task) {
                            Log.w("signUp", "createUserWithEmail:onComplete:" + task.isSuccessful());

                            registerSucceed = task.isSuccessful();
                            if(task.isSuccessful()) {
                                mAuth.getCurrentUser().sendEmailVerification();
                                createNewDatabaseUser(mNewsletterChecked);
                            }
                        }
                    });

            try {
                Thread.sleep(1000);
                if(!registerSucceed)
                    Thread.sleep(1000);
                if(!registerSucceed)
                    Thread.sleep(1000);
                if(!registerSucceed)
                    Thread.sleep(1000);
                if(!registerSucceed)
                    Thread.sleep(1000);
            } catch (InterruptedException e) {
                return false;
            }
            return registerSucceed;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                Toast.makeText(RegisterActivity.this, "Benutzer " + mAuth.getCurrentUser().getEmail() + " erstellt und erfolgreich eingeloggt", Toast.LENGTH_LONG).show();
                onClickOpenMap();
            } else {
                mPasswordView.setError(getString(R.string.error_new_user));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }

    }

    private void createNewDatabaseUser(Boolean newsletterChecked) {
        List<Integer> list = new ArrayList<>();
        Date d = new Date();
        SimpleDateFormat f = new SimpleDateFormat("dd.MM.yyyy");
        User user = new User(f.format(d.getTime()), 0,0.0001,0,0, mAuth.getCurrentUser().getEmail(), list, newsletterChecked);
        mDatabase.child("Users").child(mAuth.getCurrentUser().getUid()).setValue(user);
    }

    @Override
    public void onStart() {
        super.onStart();
        FirebaseUserActions.getInstance().start(getAction());
        mAuth.addAuthStateListener(mAuthListener);
    }

    @Override
    public void onStop() {
        FirebaseUserActions.getInstance().end(getAction());
        super.onStop();
        if (mAuthListener != null) {
            mAuth.removeAuthStateListener(mAuthListener);
        }
    }
